		<div id="faq" >
		   <h3>PREGUNTAS FRECUENTES</h3>
		   <span class="number">1</span><h4>¿Dónde está mi árbol?</h4>
		    <p>
		    	Actualmente, las siguientes Áreas de Conservación Privada son parte de Reforestamos por Naturaleza: ACP Bosque Berlín, ACP Milpuj - La Heredad, ACP Paraíso Natural Iwirati y CC Bosques del Futuro Ojos de Agua. Puedes ver dónde está ubicado tu árbol ingresando las coordenadas en un sistema de mapas en línea como Google Maps.
		    </p>
		    <span class="number">2</span><h4>¿Puedo visitar mi árbol?</h4>
		    <p>
		    	¡Sí, por favor, hazlo! En Conservamos por Naturaleza alentamos a las personas a salir a conectarse con la naturaleza. Tu llegada también es una gran contribución para las áreas de conservación donde el árbol ha sido plantado. Las visitas son una fuente importante de ingresos y apoyo moral a las personas que conservan estos lugares. Para poder hacer una visita, ingresa primero a la página de perfil del área en la que estás interesado: www.conservamos.org/explora Es muy importante coordinar con anticipación tu visita con el representante del área de conservación. Puedes contactarlo directamente con la información de contacto en su perfil o a través del equipo de Conservamos por Naturaleza.
		    </p>
		    <span class="number">3</span><h4>¿Qué especies están reforestando?</h4>
		    <p>
		    	Trabajamos con los responsables de conservación para poder elegir las especies nativas que corresponden a sus respectivas áreas de conservación. El certificado de adopción incluye el nombre común y la especie de tu árbol. 
		    </p>
		    <span class="number">4</span><h4>¿Puedo elegir en dónde y qué reforestar?</h4>
		    <p>
		    	Esta decisión es tomada por el responsable del área de conservación junto con el equipo de Conservamos por Naturaleza. Por lo general, reforestamos en áreas donde es sumamente necesario plantar nuevos árboles, debido a que el bosque tardaría mucho en recuperarse por sí solo. Nosotros solo reforestamos especies nativas de la zona.
		    </p>
		    <span class="number">5</span><h4>¿Quién se encarga del árbol?</h4>
		    <p>
		    	El responsable de conservación del área. Nosotros también enviamos equipos de voluntarios regularmente para que puedan monitorear todos estos árboles.
		    </p>
		    <span class="number">6</span><h4>¿Mi árbol me pertenece?</h4>
		    <p>
		    	A través de tu donación contribuyes con el cuidado del árbol por lo menos 3 años, pero no te pertenece. Esto no convierte en tu propiedad al árbol o a la tierra donde se ubica.
		    </p>
		    <span class="number">7</span><h4>¿Puedo usar la madera de mi árbol?</h4>
		    <p>
		    	No. Los árboles están estrictamente destinados para la conservación, para regenerar un bosque y proporcionar un hábitat o alimento a la fauna local. Los árboles nunca serán usados para proporcionar madera.  
		    </p>
		    <span class="number">8</span><h4>¿Me entregarán un certificado impreso?</h4>
		    <p>
		    	Por el momento, no, pero puedes imprimirlo en cualquier imprenta profesional y ponerlo en un marco. 
		    </p>
		    <span class="number">9</span><h4>¿Mi árbol puede morir?</h4>
		    <p>
		    	Podría pasar y es totalmente natural, aunque sea triste. Árboles jóvenes mueren por muchas razones como males naturales, vientos fuertes o lluvias. Pueden ser comidos por animales salvajes, insectos y más. Si eso pasa, vamos a replantarlo -si es posible- con un árbol de la misma especie y en el mismo lugar donde se encontraba el anterior. 
		    </p>
		    <span class="number">10</span><h4>¿Por qué S/ 50?</h4>
		    <p>
		    	Dividimos este monto en dos partes. La mitad va para el responsable del área de conservación, quien ayudará a mantener el árbol por lo menos 3 años. La otra mitad va para cubrir los gastos de la campaña como el diseño o los costos de la web, así como el monitoreo de los árboles plantados. También usamos estos fondos para ayudar a que nuevas áreas se unan al programa, esto incluye la compra de una unidad GPS, una cámara, laptop y la capacitación.
		    </p>
		    <span class="number">11</span><h4>¿Por qué solo 3 años de cuidado?</h4>
		    <p>
		    	Por lo general, en las áreas donde estamos trabajando, los árboles ya serán adultos al terminar este tiempo y necesitarán poco cuidado de ahí en adelante.
		    </p>
		    <span class="number">12</span><h4>¿Puedo elegir dónde plantarán el árbol o puedo hacer que planten árboles juntos?</h4>
		    <p>
		    	Lamentablemente no. Los árboles que se encuentran en adopción ya han sido plantados y son parte de nuestra base de datos digital. Por razones técnicas, no es posible elegir el árbol que quieras adoptar.
		    </p>
		    <span class="number">13</span><h4>¿Puedo recibir un certificado de donación para exención de impuestos?</h4>
		    <p>
		    	Sí, la SPDA está intitulada a la entidad receptora de donaciones. Podemos emitir una constancia de donación para efectos tributarios, que puede usar para motivos de impuestos. Por favor, contáctate con nosotros si es que necesitas este documento.
		    </p>
		    <span class="number">14</span><h4>¡Quiero adoptar varios árboles al mismo tiempo! ¿Cómo puedo hacerlo?</h4>
		    <p>
		    	¡Genial! De hecho, hemos trabajado con muchas organizaciones y empresas que han comprado grandes cantidades de árboles como regalos para su personal o clientes. Si estás interesado en comprar un gran lote de árboles, por favor comunícate directamente con nosotros y haremos todo lo posible para ayudarte con lo que necesites.
		    </p>
