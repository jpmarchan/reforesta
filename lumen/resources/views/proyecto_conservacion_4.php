			    <p>
			    	<b>Puerto Prado es la primera comunidad nativa que creó su propia área de conservación privada. Ellos viven en las orillas del Río Marañón, en Loreto, frente a la Reserva Nacional Pacaya Samiria y protegen 100 hectáreas de bosque. </b> </p>
<p>
La comunidad es especial no solo por haber creado su área de conservación: también es una de las pocas comunidades nativas lideradas por una mujer, Ema Tapullima. Los esfuerzos de Ema en promover la conservación y apoyar a su comunidad, han sido premiados por el Estado.</p>
<p>
Las niñas y niños de la comunidad, con el apoyo de la ONG ANIA y los colaboradores de Conservamos por Naturaleza-Loreto, han creado un <a href="https://vimeo.com/193178452" target="_blank">Bosque de Niños (BONI)</a>, donde cuidan una hectárea de bosque donada por la comunidad. Al mismo tiempo, las mujeres formaron una asociación de artesanas, para vender sus trabajos creados con la palmera chambira. 
</p>
<p>
Puerto Prado cuenta con una maloca y un baño para recibir a turistas, y ofrecen tours para que los visitantes puedan apreciar la biodiversidad de la zona. Con los fondos de Reforestamos por Naturaleza, la comunidad ha construido un sistema de tuberías que por primera vez lleva agua a sus hogares. 
</p>
<p>
Como parte de la campaña, la comunidad ha plantado árboles como el shingulo (<i>Casimerella sp</i>), el aliso (<i>Alnus acuminata</i>), la conchana (<i>Guetarda sp.</i>) y palmeras (<i>Ceroxilon sp.</i>). 
</p>

